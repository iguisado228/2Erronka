package Langileak;

public class langilea {
	
	private int idLangilea;
	private String nan, izena, abizena, lanPostua, telefonoa, helbidea, postaKodea, erabiltzaileIzena, pasahitza;

	
	
	public langilea(int idLangilea, String nan, String izena, String abizena, String lanPostua, String telefonoa,
			String helbidea, String postaKodea, String erabiltzaileIzena, String pasahitza) {
		this.idLangilea = idLangilea;
		this.nan = nan;
		this.izena = izena;
		this.abizena = abizena;
		this.lanPostua = lanPostua;
		this.telefonoa = telefonoa;
		this.helbidea = helbidea;
		this.postaKodea = postaKodea;
		this.erabiltzaileIzena = erabiltzaileIzena;
		this.pasahitza = pasahitza;
	}
	
	
	public langilea() {}
	
	
	/**
	 * @return the idLangilea
	 */
	public int getIdLangilea() {
		return idLangilea;
	}
	/**
	 * @param idLangilea the idLangilea to set
	 */
	public void setIdLangilea(int idLangilea) {
		this.idLangilea = idLangilea;
	}
	/**
	 * @return the nan
	 */
	public String getNan() {
		return nan;
	}
	/**
	 * @param nan the nan to set
	 */
	public void setNan(String nan) {
		this.nan = nan;
	}
	/**
	 * @return the izena
	 */
	public String getIzena() {
		return izena;
	}
	/**
	 * @param izena the izena to set
	 */
	public void setIzena(String izena) {
		this.izena = izena;
	}
	/**
	 * @return the abizena
	 */
	public String getAbizena() {
		return abizena;
	}
	/**
	 * @param abizena the abizena to set
	 */
	public void setAbizena(String abizena) {
		this.abizena = abizena;
	}
	/**
	 * @return the lanPostua
	 */
	public String getLanPostua() {
		return lanPostua;
	}
	/**
	 * @param lanPostua the lanPostua to set
	 */
	public void setLanPostua(String lanPostua) {
		this.lanPostua = lanPostua;
	}
	/**
	 * @return the telefonoa
	 */
	public String getTelefonoa() {
		return telefonoa;
	}
	/**
	 * @param telefonoa the telefonoa to set
	 */
	public void setTelefonoa(String telefonoa) {
		this.telefonoa = telefonoa;
	}
	/**
	 * @return the helbidea
	 */
	public String getHelbidea() {
		return helbidea;
	}
	/**
	 * @param helbidea the helbidea to set
	 */
	public void setHelbidea(String helbidea) {
		this.helbidea = helbidea;
	}
	/**
	 * @return the postaKodea
	 */
	public String getPostaKodea() {
		return postaKodea;
	}
	/**
	 * @param postaKodea the postaKodea to set
	 */
	public void setPostaKodea(String postaKodea) {
		this.postaKodea = postaKodea;
	}
	/**
	 * @return the erabiltzaileIzena
	 */
	public String getErabiltzaileIzena() {
		return erabiltzaileIzena;
	}
	/**
	 * @param erabiltzaileIzena the erabiltzaileIzena to set
	 */
	public void setErabiltzaileIzena(String erabiltzaileIzena) {
		this.erabiltzaileIzena = erabiltzaileIzena;
	}
	/**
	 * @return the pasahitza
	 */
	public String getPasahitza() {
		return pasahitza;
	}
	/**
	 * @param pasahitza the pasahitza to set
	 */
	public void setPasahitza(String pasahitza) {
		this.pasahitza = pasahitza;
	}


	
	
	
}
